    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
    ContaClienteDAO conta = new ContaClienteDAO();
    ArrayList<ContaCliente> lista = new ArrayList<ContaCliente>();
    ArrayList<VendaRelatorio> relVenda = new ArrayList<VendaRelatorio>();
    int IDcliente = Integer.parseInt(IDCliente.getText());
    lista = conta.getLista(IDcliente);
    DefaultCategoryDataset dataset = new DefaultCategoryDataset();
    for(ContaCliente contcli: lista){
        VendaRelatorio venda = new VendaRelatorio();
        venda.setQuantidade(contcli.getQuantidade());
        venda.setData(contcli.getData());
        venda.setIDCombustivel(contcli.getIDCombustivel());
        venda.setValor(contcli.getValor());
        relVenda.add(venda);
    }
    for(VendaRelatorio venda : relVenda){
        dataset.addValue(venda.getQuantidade(), "Data", venda.getData());
    }
    
    
    JFreeChart chart = ChartFactory.createLineChart("Grafico De Cliente", "Data", "Valor", dataset);
    ChartPanel chartPanel = new ChartPanel(chart);
    JOptionPane.showMessageDialog(null, chartPanel);
    IDCliente.setText("");
    Nome.setText("");
    Telefone.setText("");
    }//GEN-LAST:event_jButton5ActionPerformed

