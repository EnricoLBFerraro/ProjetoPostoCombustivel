    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    ContaClienteDAO conta = new ContaClienteDAO();
    ArrayList<ContaCliente> lista = new ArrayList<ContaCliente>();
    int IDcliente = Integer.parseInt(IDCliente.getText());
    lista = conta.getLista(IDcliente);
    String resultado = "";
    for(ContaCliente contaCli : lista){
        resultado += "Quantidade: "+ contaCli.getQuantidade() + " De Combustivel: "+ contaCli.getIDCombustivel()+" Com ID = "+ contaCli.getIDCompra() + " Na data: " + contaCli.getData() +"\nValor: "+contaCli.getValor()+"\n" ;
    }
    JOptionPane.showMessageDialog(this,resultado);
    }//GEN-LAST:event_jButton3ActionPerformed

