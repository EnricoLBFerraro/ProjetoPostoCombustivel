    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    int IDcliente = Integer.parseInt(IDCliente.getText());
    int telefone = Integer.parseInt(Telefone.getText());
    try{
        ClienteDAO clientes = new ClienteDAO();
        Cliente cliente = new Cliente(IDcliente, Nome.getText(), telefone);
        clientes.adiciona(cliente);
    } catch(Exception e){
        JOptionPane.showMessageDialog(this,"Erro!");
        IDCliente.setText("");
        Nome.setText("");
        Telefone.setText("");
        return;
    }
    JOptionPane.showMessageDialog(this,"O cliente "+ Nome.getText()+" adicionado");
    IDCliente.setText("");
    Nome.setText("");
    Telefone.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

