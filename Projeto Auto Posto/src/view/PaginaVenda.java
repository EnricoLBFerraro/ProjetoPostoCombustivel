    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Venda venda;
        VendaDAO vendaDao = new VendaDAO();
        
        geraNotaFiscal nota = new geraNotaFiscal();
        try{
            int IDvenda = Integer.parseInt(IDVenda.getText());
            int IDcliente = Integer.parseInt(IDCliente.getText());
            int IDfrentista = Integer.parseInt(IDFrentista.getText());
            int IDcombustivel = Integer.parseInt(IDCombustivel.getText());
            float litros = Float.parseFloat(Litros.getText());
            venda = new Venda(IDvenda,IDcliente,IDfrentista,IDcombustivel,litros, Data.getText());
            vendaDao.venda(venda);
            JOptionPane.showMessageDialog(this,"Checando Banco de Dados");
        } catch(Exception e){
            
            JOptionPane.showMessageDialog(this,"Venda não concluida");
            IDVenda.setText(idUltimaCompraMaisUm());
            IDCliente.setText("");
            IDFrentista.setText("");
            IDCombustivel.setText("");
            Litros.setText("");
            Valor.setText("");
            UltimoID.setText("Ultimo: "+idUltimaCompra());
            return;
        }
        JOptionPane.showMessageDialog(this,"Venda concluida");
        nota.geraNotaFiscalVenda(IDVenda.getText(), Data.getText(), IDCombustivel.getText(), Litros.getText());
        IDVenda.setText(idUltimaCompraMaisUm());
        IDCliente.setText("");
        IDFrentista.setText("");
        IDCombustivel.setText("");
        Litros.setText("");
        Valor.setText("");
        UltimoID.setText("Ultimo: "+idUltimaCompra());
    }//GEN-LAST:event_jButton1ActionPerformed

