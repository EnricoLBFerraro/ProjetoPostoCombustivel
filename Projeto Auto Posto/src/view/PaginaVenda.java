    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    ContaClienteDAO contCliente = new ContaClienteDAO();
    VendaDAO vendaDAO = new VendaDAO();
    ContaCliente conta;
    geraNotaFiscal nota = new geraNotaFiscal();
    try{
        int IDcliente = Integer.parseInt(IDCliente.getText());
        int IDCompra = Integer.parseInt(IDVenda.getText());
        int IDfrentista = Integer.parseInt(IDFrentista.getText());
        float Quantidade = Float.parseFloat(Litros.getText());
        int IDcombustivel = Integer.parseInt(IDCombustivel.getText());
        float valor = Float.parseFloat(Valor.getText());
        conta = new ContaCliente(IDcliente,IDCompra,Quantidade,IDcombustivel, Data.getText(), valor);
        Venda venda;
        venda = new Venda(IDCompra,IDcliente,IDfrentista,IDcombustivel,Quantidade,Data.getText(), valor);
        vendaDAO.vendaCliente(venda);
        contCliente.adiciona(conta);
        JOptionPane.showMessageDialog(this,"Checando Banco de Dados");
    } catch(Exception e){
        JOptionPane.showMessageDialog(this,"Venda não concluida");
        IDVenda.setText(idUltimaCompraMaisUm());
        IDCliente.setText("");
        IDFrentista.setText("");
        IDCombustivel.setText("");
        Litros.setText("");
        Valor.setText("");
        UltimoID.setText("Ultimo: "+idUltimaCompra());
        return;
    }
    JOptionPane.showMessageDialog(this,"Venda para o cliente:  "+ IDCliente.getText()+ " Concluida");
    nota.geraNotaFiscalVendaCliente(IDVenda.getText(), Data.getText(), IDCombustivel.getText(), Litros.getText(),Valor.getText());
    IDVenda.setText(idUltimaCompraMaisUm());
    IDCliente.setText("");
    IDFrentista.setText("");
    IDCombustivel.setText("");
    Litros.setText("");
    Valor.setText("");
    UltimoID.setText("Ultimo: "+idUltimaCompra());
    }//GEN-LAST:event_jButton3ActionPerformed

